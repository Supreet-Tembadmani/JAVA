public class Character_pattern {
    public static void main(String[] args) {
        char ch = 'A';
        for(int i = 1 ; i <=5; i++){
            for(int num = 1 ; num <=i; num++){
                System.out.print(ch);
                ch++;
            }
            System.out.println();

        }
    }
    
}
